	<p>Quelques infos sur Datamaps !</br>
	<div class="info">
		<p>Datamaps est avant tout un site de cartographie.<br>
		L'onglet 'la carte' vous permettra de visualiser les stations velo'v de la ville de Lyon en temps réel.<br>
		En haut à droite de la carte vous aurez accès aux <b>filtres</b> pour afficher uniquement les stations désirées ainsi que différents fond de cartes.<br>
		En haut à gauche de la carte vous aurez accès au mode <b>plein écran</b>, à une <b>recherche</b> par numéro des stations.<br><br>
		Le bandeau des outils à gauche vous permettra d'afficher la carte dans un <b>nouvel onglet</b>, de switcher entre la carte et un affichage sous forme de <b>liste des données</b>,
		d'afficher ou non les <b>relations</b> entre stations.<br>
		La fonctionnalité 'itinéraire' vous permettra quant à elle à séléctionner 2 points sur la carte, et vous affichera les 2 stations les plus adéquates à utiliser
		ainsi que la distance à éffectuer à pieds et en velo.<br><br>
		L'onglet 'archives' vous permet de visualiser quelques <b>gifs</b> plus ou moins anciens.<br>
		L'onglet 'graphes' vous permet de visualiser <b>l'utilisation d'une station</b> durant la semaine ainsi que les stations les plus utilisées avec cette dernière.<br>
		L'onglet 'statistiques' vous permet d'observer différentes <b>cartes & graphes</b> représentants l'appartenance des stations à certains groupes.
		</p>
